function an()
{
    var mychar=document.getElementById("A");
    if(mychar.style.display=="block")
    {
        document.getElementById("A").style.display="none";
        document.getElementById("a").style.display="block";
    }
    else
    {
        document.getElementById("a").style.display="none";
        document.getElementById("A").style.display="block";
    }
}
